    public void load() {
        List<ClientePedidos> clientes = creacion.clientesRepo.findAllPedidos();
        
        String[] columnNames = new String[]{"Nit", "Nombre", "Id Pedido", "Estado"};

        DefaultTableModel model = new DefaultTableModel(columnNames, 0);

        clientes.forEach(producto -> {
            if (producto.getEstadoPedido() != 99 && producto.getEstadoPedido() != 0) {
                String estado = "Facturado";
            
                if (producto.getEstadoPedido() == 1) {
                    estado = "Importado";
                } else if (producto.getEstadoPedido() == 2) {
                    estado = "Local";
                } else if (producto.getEstadoPedido() == 99) {
                    estado = "ORDEN GENERADA";
                }
                
                Object[] rowData = {
                        producto.getNit(), producto.getNombre(), producto.getIdPedido(), estado
                };
                model.addRow(rowData);
            }
            
        });
        jTable1.setModel(model);
    }
